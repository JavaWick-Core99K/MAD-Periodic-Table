// COBALT (27)
func getDiscoveryCobalt() -> String {
    let info: String = """
    Cobalt was discovered by Georg Brandt, a Swedish chemist, in 1739. Brandt was attempting to prove that the ability of certain minerals to color glass blue was due to an unknown element and not to bismuth, as was commonly believed at the time. Cobalt's primary ores are cobaltite (CoAsS) and erythrite (Co3(AsO4)2). Cobalt is usually recovered as a byproduct of mining and refining nickel, silver, lead, copper and iron.
    """
    return info
}

// NICKEL (28)
func getDiscoveryNickel() -> String {
    let info: String = """
    Nickel was discovered by the Swedish chemist Axel Fredrik Cronstedt in the mineral niccolite (NiAs) in 1751. Today, most nickel is obtained from the mineral pentlandite (NiS·2FeS). Most of the world's supply of nickel is mined in the Sudbury region of Ontario, Canada. It is believed that this large deposit of nickel ore is a result of an ancient meteor impact.
    """
    return info
}

// COPPER (29)
func getDiscoveryCopper() -> String {
    let info: String = """
    Archaeological evidence suggests that people have been using copper for at least 11,000 years. Relatively easy to mine and refine, people discovered methods for extracting copper from its ores at least 7,000 years ago. The Roman Empire obtained most of its copper from the island of Cyprus, which is where copper's name originated. Today, copper is primarily obtained from the ores cuprite (CuO2), tenorite (CuO), malachite (CuO3·Cu(OH)2), chalcocite (Cu2S), covellite (CuS) and bornite (Cu6FeS4). Large deposits of copper ore are located in the United States, Chile, Zambia, Zaire, Peru and Canada.
    """
    return info
}

// ZINC (30)
func getDiscoveryZinc() -> String {
    let info: String = """
    Although zinc compounds have been used for at least 2,500 years in the production of brass, zinc wasn't recognized as a distinct element until much later. Metallic zinc was first produced in India sometime in the 1400s by heating the mineral calamine (ZnCO3) with wool. Zinc was rediscovered by Andreas Sigismund Marggraf in 1746 by heating calamine with charcoal. Today, most zinc is produced through the electrolysis of aqueous zinc sulfate (ZnSO4).
    """
    return info
}
