// VANADIUM (23)
func getDiscoveryVanadium() -> String {
    let info: String = """
    Vanadium was discovered by Andrés Manuel del Rio, a Spanish chemist, in 1801. Rio sent samples of vanadium ore and a letter describing his methods to the Institute de France in Paris, France, for analysis and confirmation. Unfortunately for Rio, his letter was lost in a shipwreck and the Institute only received his samples, which contained a brief note describing how much this new element, which Rio had named erythronium, resembled chromium. Rio withdrew his claim when he received a letter from Paris disputing his discovery. Vanadium was rediscovered by Nils Gabriel Sefstrôm, a Swedish chemist, in 1830 while analyzing samples of iron from a mine in Sweden. Vanadium was isolated by Sir Henry Enfield Roscoe, an English chemist, in 1867 by combining vanadium trichloride (VCl3) with hydrogen gas (H2). Today, vanadium is primarily obtained from the minerals vanadinite (Pb5(VO)3Cl) and carnotite (K2(UO2)2VO4·1-3H2O) by heating crushed ore in the presence of carbon and chlorine to produce vanadium trichloride. The vanadium trichloride is then heated with magnesium in an argon atmosphere.
    """
    return info
}

// CHROMIUM (24)
func getDiscoveryChromium() -> String {
    let info: String = """
    Chromium was discovered by Louis-Nicholas Vauquelin while experimenting with a material known as Siberian red lead, also known as the mineral crocoite (PbCrO4), in 1797. He produced chromium oxide (CrO3) by mixing crocoite with hydrochloric acid (HCl). Although he believed a method for isolating chromium didn't yet exist, Vauquelin was pleasantly surprised in 1798 to discover that he was able to obtain metallic chromium by simply heating chromium oxide in a charcoal oven. Today, chromium is primarily obtained by heating the mineral chromite (FeCr2O4) in the presence of aluminum or silicon.
    """
    return info
}

// MANGANESE (25)
func getDiscoveryManganese() -> String {
    let info: String = """
    Proposed to be an element by Carl Wilhelm Scheele in 1774, manganese was discovered by Johan Gottlieb Gahn, a Swedish chemist, by heating the mineral pyrolusite (MnO2) in the presence of charcoal later that year. Today, most manganese is still obtained from pyrolusite, although it is usually burned in a furnace with powdered aluminum or is treated with sulfuric acid (H2SO4) to form manganese sulfate (MnSO4), which is then electrolyzed.
    """
    return info
}

// IRON (26)
func getDiscoveryIron() -> String {
    let info: String = """
    Archaeological evidence suggests that people have been using iron for at least 5000 years. Iron is the cheapest and one of the most abundant of all metals, comprising nearly 5.6% of the earth's crust and nearly all of the earth's core. Iron is primarily obtained from the minerals hematite (Fe2O3) and magnetite (Fe3O4). The minerals taconite, limonite (FeO(OH)·nH2O) and siderite (FeCO3) are other important sources.
    """
    return info
