// HAFNIUM (72)
func getDiscoveryHafnium() -> String {
    let info: String = """
    Hafnium was discovered by Dirk Coster, a Danish chemist, and George Charles de Hevesy, a Hungarian chemist, in 1923. They used a method known as X-ray spectroscopy to study the arrangement of the outer electrons of atoms in samples of zirconium ore. The electron structure of hafnium had been predicted by Niels Bohr and Coster and Hevesy found a pattern that matched. Hafnium is difficult to separate from zirconium and is present in all of its ores. It is obtained with the same methods used to extract zirconium.
    """
    return info
}

// TANTALUM (73)
func getDiscoveryTantalum() -> String {
    let info: String = """
    Tantalum was discovered by Anders Gustaf Ekenberg, a Swedish chemist, in 1802 in minerals obtained from Ytterby, Sweden. Many scientists believed that he had only discovered an allotrope of niobium, an element that is chemically similar to tantalum. The issue was finally settled in 1866 when, Jean Charles Galissard de Marignac, a Swiss chemist, proved that tantalum and niobium were two distinct elements. The first relatively pure samples of tantalum were first produced in 1907. Today, tantalum is primarily obtained from the minerals columbite ((Fe, Mn, Mg)(Nb, Ta)2O6), tantalite ((Fe, Mn)(Ta, Nb)2O6) and euxenite ((Y, Ca, Er, La, Ce, U, Th)(Nb, Ta, Ti)2O6).
    """
    return info
}

// TUNGSTEN (74)
func getDiscoveryTungsten() -> String {
    let info: String = """
    Tungsten was discovered by Juan José and Fausto Elhuyar, Spanish chemists and brothers, in 1783 in samples of the mineral wolframite ((Fe, Mn)WO4). Today, tungsten is primarily obtained from wolframite and scheelite (CaWO4) using the same basic method developed by José and Elhuyar. Tungsten ores are crushed, cleaned and treated with alkalis to form tungsten trioxide (WO3). Tungsten trioxide is then heated with carbon or hydrogen gas (H2), forming tungsten metal and carbon dioxide (CO2) or tungsten metal and water vapor (H2O).
    """
    return info
}

// RHENIUM (75)
func getDiscoveryRhenium() -> String {
    let info: String = """
    Rhenium was discovered by the German chemists Ida Tacke-Noddack, Walter Noddack and Otto Carl Berg in 1925. They detected rhenium spectroscopically in platinum ores and in the minerals columbite ((Fe, Mn, Mg)(Nb, Ta)2O6), gadolinite ((Ce, La, Nd, Y)2FeBe2Si2O10) and molybdenite (MoS2). Rhenium is present in these materials only in trace amounts. In 1928, Noddack and Berg were able to extract 1 gram of rhenium from 660 kilograms of molybdenite. Today, rhenium is obtained as a byproduct of refining molybdenum and copper.
    """
    return info
}

