// PROMETHIUM (61)
func getDiscoveryPromethium() -> String {
    let info: String = """
    The existence of promethium was predicted by Bohuslav Brauner, a Czech chemist, in 1902. Several groups claimed to have produced the element, but they could not confirm their discoveries because of the difficulty of separating promethium from other elements. Proof of the existence of promethium was obtained by Jacob A. Marinsky, Lawrence E. Glendenin and Charles D. Coryell in 1944. Too busy with defense related research in World War II, they did not claim their discovery until 1946. They discovered promethium while analyzing the byproducts of uranium fission that were produced in a nuclear reactor located at Clinton Laboratories in Oak Ridge, Tennessee. Today, Clinton Laboratories is known as Oak Ridge National Laboratory. Promethium does not occur naturally on earth, although it has been detected in the spectrum of a star in the constellation Andromeda.
    """
    return info
}

// SAMARIUM (62)
func getDiscoverySamarium() -> String {
    let info: String = """
    Samarium was observed spectroscopically by Jean Charles Galissard de Marignac, a Swiss chemist, in a material known as dydimia in 1853. Paul-Émile Lecoq de Boisbaudran, a French chemist, was the first to isolate samarium from the mineral samarskite ((Y, Ce, U, Fe)3(Nb, Ta, Ti)5O16) in 1879. Today, samarium is primarily obtained through an ion exchange process from monazite sand ((Ce, La, Th, Nd, Y)PO4), a material rich in rare earth elements that can contain as much as 2.8% samarium.
    """
    return info
}

